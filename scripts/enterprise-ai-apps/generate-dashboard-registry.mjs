#!/usr/bin/env node
/**
 * Merge dashboard presets into enterprise-ai-apps-registry.json and emit TypeScript constants.
 * Usage: node scripts/enterprise-ai-apps/generate-dashboard-registry.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { APP_BRANDING } from "./agent-branding.mjs";
import { DASHBOARD_KPI_BY_APP } from "./dashboard-kpi-catalog.mjs";
import { DASHBOARD_PRESETS } from "./dashboard-presets.mjs";
import { loadRegistry, REGISTRY_PATH, ROOT } from "./registry.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUT_TS = path.join(ROOT, "packages/@buildingai/constants/src/shared/enterprise-dashboard.constant.ts");

function buildDashboardBlock(app) {
    const preset = DASHBOARD_PRESETS[app.appId];
    if (!preset) {
        throw new Error(`Missing dashboard preset for ${app.appId}`);
    }
    const kpis = DASHBOARD_KPI_BY_APP[app.appId];
    if (!kpis || kpis.length !== 6) {
        throw new Error(`Expected 6 KPI slots for ${app.appId}, got ${kpis?.length ?? 0}`);
    }
    const branding = APP_BRANDING[app.appId];
    return {
        template: preset.template,
        hue: branding?.hue ?? 199,
        icon: branding?.icon ?? "data-health",
        healthScoreLabel: preset.healthScoreLabel,
        batchLabel: preset.batchLabel ?? "检查批次",
        heroChart: preset.heroChart,
        accentChart: preset.accentChart ?? "riskDonut",
        domainChartTitle: preset.domainChartTitle ?? "业务域异常分布",
        trendChartTitle: preset.trendChartTitle ?? "近14天异常趋势",
        kpis,
        tablePrefix: app.tablePrefix,
    };
}

function main() {
    const registry = loadRegistry();
    for (const app of registry.apps) {
        app.dashboard = buildDashboardBlock(app);
    }
    fs.writeFileSync(REGISTRY_PATH, `${JSON.stringify(registry, null, 2)}\n`, "utf8");

    const lines = [
        "/** Auto-generated by scripts/enterprise-ai-apps/generate-dashboard-registry.mjs */",
        "",
        'export const ENTERPRISE_DASHBOARD_TEMPLATE_IDS = [',
        '    "supply-chain",',
        '    "compliance-audit",',
        '    "finance",',
        '    "operations",',
        '    "project-service",',
        '    "sustainability",',
        "] as const;",
        "",
        "export type EnterpriseDashboardTemplateId = (typeof ENTERPRISE_DASHBOARD_TEMPLATE_IDS)[number];",
        "",
        "export type EnterpriseDashboardKpiSlot = {",
        "    metric: string;",
        "    label: string;",
        "};",
        "",
        "export type EnterpriseDashboardChartId =",
        '    | "trendArea"',
        '    | "riskDonut"',
        '    | "domainBars"',
        '    | "domainQuadrant"',
        '    | "domainEsgBars"',
        '    | "topRules"',
        '    | "statusPie"',
        '    | "batchStack"',
        '    | "batchTimeline"',
        '    | "recentBatches"',
        '    | "agingStack"',
        '    | "repairTrend";',
        "",
        "export type EnterpriseDashboardConfig = {",
        "    appId: string;",
        "    template: EnterpriseDashboardTemplateId;",
        "    hue: number;",
        "    icon: string;",
        "    healthScoreLabel: string;",
        "    batchLabel: string;",
        "    heroChart: EnterpriseDashboardChartId;",
        "    accentChart: EnterpriseDashboardChartId;",
        "    domainChartTitle: string;",
        "    trendChartTitle: string;",
        "    kpis: EnterpriseDashboardKpiSlot[];",
        "    tablePrefix: string;",
        "};",
        "",
        "export const ENTERPRISE_DASHBOARD_BY_APP_ID: Record<string, EnterpriseDashboardConfig> = {",
    ];

    for (const app of registry.apps) {
        const d = app.dashboard;
        lines.push(`    "${app.appId}": ${JSON.stringify({ appId: app.appId, ...d }, null, 8).replace(/^/gm, "    ").trim()},`);
    }
    lines.push("};");
    lines.push("");
    lines.push("export function getEnterpriseDashboardConfig(appId: string): EnterpriseDashboardConfig | undefined {");
    lines.push("    return ENTERPRISE_DASHBOARD_BY_APP_ID[appId];");
    lines.push("}");
    lines.push("");

    fs.mkdirSync(path.dirname(OUT_TS), { recursive: true });
    fs.writeFileSync(OUT_TS, lines.join("\n"), "utf8");
    console.log(`Updated ${REGISTRY_PATH}`);
    console.log(`Wrote ${OUT_TS}`);
    console.log(`Apps: ${registry.apps.length}`);
}

main();
